% Este archivo implementa la formula del area de un triangulo
% como funcion de la base de de la altura
% la funcion tiene pues dos argumentos de entrada : b h
%
% Input
%	b = base
%	h = altura
% Output
%	y = area del triangulo

function y = area_triangulo(b, h)
	y = b*h/2;
endfunction